#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*d;

	len = ft_strlen(s1);
	if (!(d = (char *)malloc(len + 1)))
		return (NULL);
	d[len] = '\0';
	while (len--)
		d[len] = s1[len];
	return (d);
}
